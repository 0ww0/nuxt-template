#!/usr/bin/env node
// Regenerates the rate-limit TABLE in RATE_LIMITS.md from the actual
// checkRateLimit(...) calls in server/api/. The prose around the table is
// hand-written and preserved; only the block between the BEGIN/END markers is
// replaced.
//
//   Update:  node scripts/gen-rate-limits.mjs
//   CI:      run it, then `git diff --exit-code RATE_LIMITS.md` (fail on drift)
//
// Catches both missing rows AND wrong numbers — the two ways the doc drifted.
import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs'
import { join } from 'node:path'

const API_DIR = 'server/api'
const MD = 'RATE_LIMITS.md'
const BEGIN = '<!-- BEGIN:rate-table (generated by scripts/gen-rate-limits.mjs — do not edit by hand) -->'
const END = '<!-- END:rate-table -->'

function walk(dir) {
  const out = []
  let entries
  try { entries = readdirSync(dir) } catch { return out }
  for (const name of entries) {
    const p = join(dir, name)
    if (statSync(p).isDirectory()) out.push(...walk(p))
    else if (name.endsWith('.ts')) out.push(p)
  }
  return out
}

// Balanced read of the (...) starting at the index of '('.
function readArgs(src, openIdx) {
  let depth = 0
  for (let i = openIdx; i < src.length; i++) {
    const c = src[i]
    if (c === '(') depth++
    else if (c === ')' && --depth === 0) return src.slice(openIdx + 1, i)
  }
  return null
}

// Split top-level commas, respecting (), {}, [].
function splitArgs(s) {
  const args = []
  let depth = 0, last = 0
  for (let i = 0; i < s.length; i++) {
    const c = s[i]
    if ('({['.includes(c)) depth++
    else if (')}]'.includes(c)) depth--
    else if (c === ',' && depth === 0) { args.push(s.slice(last, i)); last = i + 1 }
  }
  args.push(s.slice(last))
  return args.map((a) => a.trim())
}

// Evaluate simple numeric expressions like "15 * 60_000".
function num(expr) {
  const cleaned = String(expr).replace(/_/g, '').trim()
  if (!/^[\d*+\-\s]+$/.test(cleaned)) return null
  try { return Function(`"use strict";return (${cleaned})`)() } catch { return null }
}

const minutes = (ms) => (ms == null ? '?' : `${Math.round(ms / 60000)} min`)

function policyOf(objStr) {
  const get = (k) => {
    const m = objStr.match(new RegExp(k + '\\s*:\\s*([^,}]+)'))
    return m ? num(m[1]) : null
  }
  return { maxAttempts: get('maxAttempts'), windowMs: get('windowMs'), lockoutMs: get('lockoutMs') }
}

function accountLabel(expr) {
  if (!expr) return null
  if (/email/i.test(expr)) return 'email'
  if (/user\.?id/i.test(expr)) return 'userId'
  if (/token/i.test(expr)) return 'token hash'
  return expr.replace(/[`'"]/g, '')
}

function routeOf(file) {
  let p = file.replace(/\\/g, '/').replace(/^server\/api/, '/api').replace(/\.ts$/, '')
  const m = p.match(/\.(get|post|put|patch|delete)$/)
  const method = (m ? m[1] : 'post').toUpperCase()
  p = p.replace(/\.(get|post|put|patch|delete)$/, '').replace(/\/index$/, '')
  return `${method} ${p}`
}

const rows = []
for (const file of walk(API_DIR)) {
  const src = readFileSync(file, 'utf8')
  let i = 0
  while ((i = src.indexOf('checkRateLimit', i)) !== -1) {
    const open = src.indexOf('(', i)
    i += 'checkRateLimit'.length
    const argStr = open === -1 ? null : readArgs(src, open)
    if (!argStr) continue
    const args = splitArgs(argStr)            // event, 'action', {policy}, accountKey?
    const am = (args[1] || '').match(/['"]([^'"]+)['"]/)
    if (!am) continue
    const pol = policyOf(args[2] || '')
    const acct = accountLabel(args[3])
    const ip =
      `${pol.maxAttempts ?? '?'} / ${minutes(pol.windowMs)}` +
      (pol.lockoutMs ? `, ${minutes(pol.lockoutMs)} lockout` : '')
    // The edge util runs the account bucket at HALF maxAttempts, same window.
    const account =
      acct == null ? '—'
      : pol.maxAttempts ? `${acct} (${Math.ceil(pol.maxAttempts / 2)} / ${minutes(pol.windowMs)})`
      : acct
    rows.push({ route: routeOf(file), action: am[1], ip, account })
  }
}

rows.sort((a, b) => a.route.localeCompare(b.route))

const table = [
  '| Endpoint | Action key | IP policy | Account bucket |',
  '|---|---|---|---|',
  ...rows.map((r) => `| \`${r.route}\` | \`${r.action}\` | ${r.ip} | ${r.account} |`),
].join('\n')

let md = readFileSync(MD, 'utf8')
const b = md.indexOf(BEGIN)
const e = md.indexOf(END)
if (b === -1 || e === -1) {
  console.error(`ERROR: ${MD} is missing the ${BEGIN} / ${END} markers.`)
  process.exit(2)
}
md = md.slice(0, b + BEGIN.length) + '\n\n' + table + '\n\n' + md.slice(e)
writeFileSync(MD, md)
console.log(`Wrote ${rows.length} rate-limit rows to ${MD}`)
